let n = 10;
for (let i = 1; i <= n; i++) {
    let result = " ";
    console.log(result);
    for (let j = 1; j <= n; j++) {
        result = result + j + " ";
    }
    console.log(result);
}
let x = 10;
for (let i = 1; i <= x; i++) {
    let result = " ";
    console.log(result);
    for (let j = 1; j <= x; j++) {
        result = result + "* ";
    }
    console.log(result);
}